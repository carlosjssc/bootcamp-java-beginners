public class TiposConstante {
    
    public static void main (String[] args)
    {

        int n1 = 22;
        int n2 = 10;
        System.out.println(somar(n1, n2));

    }
    
    static int somar(int numero1, int numero2)
        {
            int soma = numero1 + numero2;
            return soma;
        }

}
